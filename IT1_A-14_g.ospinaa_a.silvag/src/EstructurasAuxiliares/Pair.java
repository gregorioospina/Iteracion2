package EstructurasAuxiliares;

public class Pair {

	Object object;
	String string;
	
	public Pair(Object object, String string) {
		this.object = object;
		this.string = string;
	}
	/**
	 * @return the object
	 */
	public Object getObject() {
		return object;
	}
	/**
	 * @param object the object to set
	 */
	public void setObject(Object object) {
		this.object = object;
	}
	/**
	 * @return the string
	 */
	public String getString() {
		return string;
	}
	/**
	 * @param string the string to set
	 */
	public void setString(String string) {
		this.string = string;
	}
	
	
	
}
